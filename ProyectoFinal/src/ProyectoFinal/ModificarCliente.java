    public void llenarRutas(ArrayList<Integer> ID_rutas){
        CClientes c = new CClientes();
        try{
            try (Statement stmt = c.conexion.createStatement()) {
                String consulta = "select * from rutas";
                try (ResultSet rs = stmt.executeQuery(consulta)) {
                    while (rs.next()) {
                        ID_rutas.add(rs.getInt("ID_ruta"));
                        jComboBox1.addItem(rs.getString("nombre_ruta"));
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("No se pudo acceder a la tabla");
        }
    }
    
    public void llenarTipos(ArrayList<Integer> ID_tipos){
        CClientes c = new CClientes();
        try{
            try (Statement stmt = c.conexion.createStatement()) {
                String consulta = "select * from tipos_cliente";
                try (ResultSet rs = stmt.executeQuery(consulta)) {
                    while (rs.next()) {
                        ID_tipos.add(rs.getInt("ID_tipo"));
                        jComboBox2.addItem(rs.getString("tipo_cliente"));
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("No se pudo acceder a la tabla");
        }
    }

