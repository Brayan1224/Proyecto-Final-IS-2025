    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        CClientes c = new CClientes();
        String nombre_cliente = jTextField1.getText();
        String paterno = jTextField2.getText();
        String materno = jTextField3.getText();
        String telefono = jTextField4.getText();
        String cp = jTextField5.getText();
        String direccion = jTextArea1.getText();
        int ruta = jComboBox1.getSelectedIndex();
        int tipo = jComboBox2.getSelectedIndex();
        if(nombre_cliente.isBlank() ||
                paterno.isBlank() ||
                materno.isBlank() ||
                telefono.isBlank() ||
                cp.isBlank() ||
                direccion.isBlank() ||
                ruta == 0 ||
                tipo == 0){
            JOptionPane.showMessageDialog(this, "Llenar todos los campos");
        }else{
            c.capturar_id_cliente(this.id_cliente);
            c.actualizar("nombre_cliente", nombre_cliente, 1);
            c.actualizar("ap_pat_cliente", paterno, 1);
            c.actualizar("ap_mat_cliente", materno, 1);
            c.actualizar("telefono_cliente", telefono, 1);
            c.actualizar("cp", cp, 1);
            c.actualizar("calles", direccion, 1);
            c.actualizar("R_ruta", ID_rutas.get(ruta-1)+"", 2);
            c.actualizar("R_tipo", ID_tipos.get(tipo-1)+"", 2);
            JOptionPane.showMessageDialog(this, "Informacion del cliente modificada");
            Clientes newframe = new Clientes();
            newframe.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

