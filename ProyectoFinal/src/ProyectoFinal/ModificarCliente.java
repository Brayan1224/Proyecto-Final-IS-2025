    public ModificarCliente(int id) {
        initComponents();
        this.setLocationRelativeTo(this);
        SetImageLabel(jLabel1,"src\\imagenes\\logotip.png");
        SetImageLabel(jLabel2,"src\\imagenes\\banner1.jpg");
        SetImageLabel(jLabel6,"src\\imagenes\\mandala.png");
        llenarRutas(ID_rutas);
        llenarTipos(ID_tipos);
        this.id_cliente = -1;
        CClientes c = new CClientes();
        try{
            try (Statement stmt = c.conexion.createStatement()) {
                String consulta = "select * from clientes where ID_cliente = " + id;
                try (ResultSet rs = stmt.executeQuery(consulta)) {
                    if (rs.next()) {
                        jTextField1.setText(rs.getString("nombre_cliente"));
                        jTextField2.setText(rs.getString("ap_pat_cliente"));
                        jTextField3.setText(rs.getString("ap_mat_cliente"));
                        jTextField4.setText(rs.getString("telefono_cliente"));
                        jTextField5.setText(rs.getString("cp"));
                        jTextArea1.setText(rs.getString("calles"));
                        int ruta = rs.getInt("R_ruta");
                        for (int i = 0; i < ID_rutas.size(); i++){
                            if(ruta == ID_rutas.get(i)){
                                jComboBox1.setSelectedIndex(i+1);
                            }
                        }
                        int tipo = rs.getInt("R_tipo");
                        for (int i = 0; i < ID_tipos.size(); i++){
                            System.out.println(tipo +" "+ID_tipos.get(i));
                            if(tipo == ID_tipos.get(i)){
                                jComboBox2.setSelectedIndex(i+1);
                            }
                        }
                        this.id_cliente = id;
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("No se pudo acceder a la tabla");
        }
    }

