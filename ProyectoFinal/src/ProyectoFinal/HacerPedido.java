    public void mostrar(String filtros){
        CProductos p = new CProductos();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No. Producto");
        model.addColumn("Nombre");
        model.addColumn("Cantidad");
        visor1.setModel(model);
        String datos[] = new String[3];
        try{
            try (Statement stmt = p.conexion.createStatement()) {
                String consulta = "select ID_producto, descripcion_producto, piezas_disponibles from productos "+ filtros;
                try (ResultSet rs = stmt.executeQuery(consulta)) {
                    while (rs.next()) {
                        datos[0] = rs.getString("ID_producto");
                        datos[1] = rs.getString("descripcion_producto");
                        datos[2] = rs.getString("piezas_disponibles");
                        model.addRow(datos);
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("No se pudo acceder a la tabla");
        }
    }

