    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = visor1.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }else{
            int id_producto = Integer.parseInt((String) visor1.getValueAt(fila, 0));
            int cantidad;
            try {
                cantidad = Integer.parseInt(jTextField2.getText());
                String [] arrCarrito = new String[2]; 
                arrCarrito[0] = id_producto+"";
                arrCarrito[1] = cantidad+"";
                model1.addRow(arrCarrito);
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, "Ingrese un numero");
            }
            jTextField2.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        CProductos p = new CProductos();
        CPedidos pedido = new CPedidos();
        int rowCount = visor2.getRowCount();
        ArrayList<Integer> productos = new ArrayList();
        ArrayList<Integer> cantidades = new ArrayList();
        for (int row = 0; row < rowCount; row++) {
                int no_producto = Integer.parseInt((String)visor2.getValueAt(row, 0));
                int cantidad = Integer.parseInt((String)visor2.getValueAt(row, 1));
                p.capturar_id_producto(no_producto);
                if(p.vender(cantidad) == 1){
                    productos.add(no_producto);
                    cantidades.add(cantidad);
                }
        }
        pedido.generarPedido(LocalDate.now()+"", jTextField3.getText(), id_cliente, 1, productos, cantidades);
        JOptionPane.showMessageDialog(this, "Pedido realizado");
        this.dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

